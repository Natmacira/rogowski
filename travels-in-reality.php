        We’ve travelled so far into hyperreality…. We’ve surrounded ourselves with props and reproductions so that we no longer need to dream of the real. The format of this series is a homage to Kodak Nr. 1, the first camera available to the public. This brown box, introduced in 1888, produced only circle images. Through the process of creating these photographs I was attempting to recreate what it might have been like to take pictures back in 19th century. My plastic Polaroid, just like the Kodak Nr. 1, was extremely technically limited. The results are a contemplation on how photographs of the contemporary world might look like if photography was something novel, precious or even magical.
